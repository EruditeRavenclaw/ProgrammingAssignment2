##This function creates a matrix in order to cache its inverse

makeCacheMatrix <- function(x = matrix()) { m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setmean <- function(mean) m <<- mean
        getmean <- function() m
        list(set = set, get = get,
             setmean = setmean,
             getmean = getmean)

}


## An object matrix is created by this function to cache its inverse

cacheSolve <- function(x, ...) {
            m <- x$getmean()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- mean(data, ...)
        x$setmean(m)
        
}
